// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  
  let count = 0;
  
  for(let i = 0; i < string.length; i++) {
    if(char === string[i]) {
      count += 1;
    }
  }
  
  return count;

}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  
  let count = 0;
  
  for(let i = 0; i < string.length; i++) {
    if(string[i] === 'B') {
      count += 1;
    }
  }
  
  return count;

}

