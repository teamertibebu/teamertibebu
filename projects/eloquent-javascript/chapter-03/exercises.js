function min(num1, num2) {

  return Math.min(num1, num2)

}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {

  num = Math.abs(num);

  if(num === 0) {
    return true;
  } else if(num === 1) {
    return false;
  } else {
    return isEven(num - 2);
  }

}

